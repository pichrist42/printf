#include "../includes/ft_printf.h"

unsigned int ft_nbrlen(int d)
{
	int i = 0;
	if (d < 0)
	{
		++i;
		d = -d;
	}
	while (d > 10)
	{
		++i;
		d /= 10;
	}
	return (i + 1);
}
